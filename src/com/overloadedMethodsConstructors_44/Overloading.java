public class Overloading {
    public static void main(String[] args) {

        System.out.println(OverloadedMethods.giveAnumber(1));
        System.out.println(OverloadedMethods.giveAnumber(2.3));
        System.out.println(OverloadedMethods.giveAnumber(2, 5));
        System.out.println(OverloadedMethods.giveAnumber(3.14, 6));
        System.out.println(OverloadedMethods.giveAnumber(3, 2.68));
        System.out.println(OverloadedMethods.giveAnumber(1.02, 4,3));


    }

}
